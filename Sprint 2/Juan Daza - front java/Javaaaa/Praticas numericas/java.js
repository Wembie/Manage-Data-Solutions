document.write('Valor de a: ', a);
document.write('<br>');
a+=8; //Aumento variable (incremento)
document.write('Valor de a: ', a);
document.write('<br>')
a--; //resta valor en 1 (decremento)
document.write('Valor de a: ', a)
document.write('<br>')
a-=1;// resta variable
